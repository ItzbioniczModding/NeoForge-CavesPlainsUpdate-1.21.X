public class ModConfiguredFeatures {

    public static final ResourceKey<ConfiguredFeature<?, ?>> HOLLOW_KEY = registerKey("hollow");

    public static void bootstrap(BootstrapContext<ConfiguredFeature<?, ?>> context) {



        register(context, HOLLOW_KEY, Feature.TREE, new TreeConfiguration.TreeConfigurationBuilder(
                BlockStateProvider.simple(ModBlocks.HOLLOW_LOG.get()),
                new ForkingTrunkPlacer(4, 0, 0),

                BlockStateProvider.simple(ModBlocks.HOLLOW_LEAVES.get()),
                new BlobFoliagePlacer(ConstantInt.of(2), ConstantInt.of(2), 3),

                new TwoLayersFeatureSize(0, 0, 0)).forceDirt().build());

    }

    public static ResourceKey<ConfiguredFeature<?, ?>> registerKey(String name) {
        return ResourceKey.create(Registries.CONFIGURED_FEATURE, ResourceLocation.fromNamespaceAndPath(CpuMod.MOD_ID, name));
    }

    private static <FC extends FeatureConfiguration, F extends Feature<FC>> void register(BootstrapContext<ConfiguredFeature<?, ?>> context,
                                                                                          ResourceKey<ConfiguredFeature<?, ?>> key, F feature, FC configuration) {
        context.register(key, new ConfiguredFeature<>(feature, configuration));
    }
}
